this_list = ["apple", "banana", "cherry"]
print(this_list[1])  # access element at index 1
print(this_list[-1])  # access the  element from reverse order the (last element)
# access the range of indexes
this_list = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(this_list)
print(this_list[2:5])  # the second index is included the 5th index is excluded
this_list = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(this_list[:4])  # prints all the elements from the beginning excluding "kiwi"
this_list = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(this_list[2:])  # prints all the elements from the second index
