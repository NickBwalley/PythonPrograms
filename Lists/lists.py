
friends = ["Seth", "Strowman", "Alexa", "Aleister", "Charlotte", "Becky"]
print (friends)#prints out the whole list
print(friends[0])#prints out the first element of the list
print(friends[-1])#access elements from the last element
print(friends[1:])#prints element from the 1st index
print(friends[1:3])#prints value from one to three excluding three
friends[0] = "Lashley" #value index 1 will be overridden
print(friends[0:5])