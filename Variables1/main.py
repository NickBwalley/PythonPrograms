'''
CASTING IN PYTHON: If you want to specify the data type of a variable, this can be done with casting.
'''
x = str(3)
y = int(3)
z = float(3)
print(x)
print(y)
print(z)
# GET THE TYPE OF VARIABLE
print(type(x))
print(type(y))
print(type(z))
